package dsv.buecher;

public class ParallellPhrase {
	public String de;
	public String es;
	public String data = "";;

	public ParallellPhrase(String de, String es, String data) {
		super();
		this.de = de;
		this.es = es;
		this.data = data;
	}

	public ParallellPhrase(String de, String es) {
		super();
		this.de = de;
		this.es = es;
	}

}
